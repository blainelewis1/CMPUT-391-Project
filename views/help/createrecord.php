		<h1>Uploading Module</h1>
			<p>
				The Uploading module is used to enter a radiology record into the database.
			</p>
			<p>
				To enter a record, the Radiologist should navigate to the Uploading page.
			</p>
			<p>
				Fill in all of the information and press submit.
			</p>
			<p>
				Then upload one or many image(s) by clicking choose file.
			</p>
			<p>
				Add another image by pressing submit another.
			</p>
			<p>
				Press submit and finish when all images have been added.
			</p>
