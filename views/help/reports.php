		<h1>Report Generating Module</h1>
			<p>
				The Report Generating page is used to get a list of patients with a specified diagnosis within a given time period.
			</p>
			<p>
				To get a report, the Admin can enter a diagnosis, a start date, and/or an end date.
			</p>
			<p>
				If the Admin enters all three, it will return a table with information on radiology records that contain that exact diagnosis and fall between the two dates.
			</p>
			<p>
				If the Admin only enters a diagnosis, it will display all records with that diagnosis.
			</p>
			<p>
				If the Admin only enters a start date, the returned records will be between the entered date and today.
			</p>
			<p>
				If the Admin only enters an end date, the returned records will be all records prior to the entered date.
			</p>
			<p>
				Press Search to generate the report. The results will be shown in the table below.
			</p>
