		<h1>User Management Module</h1>
			<p>
				The User Management page can be used to create/edit persons, users, and family doctors.
			</p>
			<p>
				It can also be used to delete users and family doctors.
			</p>
			<p>
				To create a person:
			</p>
			<div>
				<p>
					Navigate to the Persons tab.
				</p>
				<p>
					Click "Create Person".
				</p>
				<p>
					Fill in the information (set person id to the next highest number).
				</p>
				<p>
					Press submit.
				</p>
				<p>
					The newly created person should be shown in the table.
				</p>
			</div>
			<p>
				To create a user:
			</p>
			<div>
				<p>
					Navigate to the User tab.
				</p>
				<p>
					Click "New User".
				</p>
				<p>
					Fill in the information (a person must be created first).
				</p>
				<p>
					This is where you choose the class of the user (admin, doctor, radiologist, patient).
				</p>
				<p>
					Press submit.
				</p>
			</div>
			<p>
				To create a family doctor:
			</p>
			<div>
				<p>
					Navigate to the Family Doctor tab.
				</p>
				<p>
					Click "New Family Doctor".
				</p>
				<p>
					Choose the doctor and patient.
				</p>
				<p>
					Press submit.
				</p>
			</div>
			<p>
				To edit any of the above, click the edit icon at the end of the row.
			</p>
			<p>
				To delete a user or a family doctor, navigate to the appropriate tab and click the delete icon.
			</p>
	</body>
